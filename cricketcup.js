for(let i = 0 ; i < ROWS ; ++i){
    for(let j = 0 ; j < COLS ; ++j){
	ctx.beginPath();
	if(scene[j][i] === 1){
	    ctx.fillStyle = "red"
	}else{
	    ctx.fillStyle = "#77b300"
	}
	ctx.rect(i , j , 1 , 1);
	ctx.fill();
    }
}



